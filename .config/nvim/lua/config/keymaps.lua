vim.g.mapleader = " "

-- open file search
vim.keymap.set("n", "<leader>pv", vim.cmd.Ex, { desc = "File search" })
vim.keymap.set("v", "J", ":m '>+1<CR>gv=gv")
vim.keymap.set("v", "K", ":m '<-2<CR>gv=gv")

